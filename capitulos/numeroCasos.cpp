#include<bits/stdc++.h> // Incluir todas las bibliotecas estándar necesarias

using namespace std;

int main() {
    int cases; // Declarar una variable para almacenar el número de casos
    cin >> cases; // Leer el número de casos desde la entrada estándar

    // Iterar sobre cada caso
    while (cases--) {
        int a, b, c; // Declarar variables para almacenar los valores de entrada para cada caso
        cin >> a >> b >> c; // Leer los valores de entrada para el caso actual
        cout << a << " " << b << " " << c << endl; // Imprimir los valores de entrada para el caso actual
    }

    return 0;
}
